package ipn;

public class NoAffiliateException extends RuntimeException {

	public NoAffiliateException() {
		super();
	}

	public NoAffiliateException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public NoAffiliateException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NoAffiliateException(String arg0) {
		super(arg0);
	}

	public NoAffiliateException(Throwable arg0) {
		super(arg0);
	}

}
